    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        // delete
        $role  = roles::find($id);
        $role->delete();

        // redirect
        Session::flash('message', 'Successfully deleted the role!');
